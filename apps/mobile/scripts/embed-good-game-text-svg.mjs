#!/usr/bin/env node
/**
 * Regenerates `src/components/goodGameTextSvgXml.ts` from `assets/brand/good-game-text.svg`.
 * Run from apps/mobile: `node scripts/embed-good-game-text-svg.mjs`
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const svgPath = path.join(root, "assets/brand/good-game-text.svg");
const outPath = path.join(root, "src/components/goodGameTextSvgXml.ts");
const svg = fs.readFileSync(svgPath, "utf8");
const out =
  "// Generated by scripts/embed-good-game-text-svg.mjs\n" +
  "export const GOOD_GAME_TEXT_SVG_XML = " +
  JSON.stringify(svg) +
  ";\n";
fs.writeFileSync(outPath, out);
console.log("Wrote", outPath, `(${out.length} bytes)`);
